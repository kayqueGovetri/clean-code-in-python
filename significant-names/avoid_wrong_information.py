# Informações erradas no nome da variável pode induzir erros.
# Nomes de variáveis ruins seriam: "hp", "aix" e "sco", pois são nomes relacionados a plataformas Unixs.
# Se estiver a calcular a hipotenusa pode confundir outros programadores com a abreviação "hp".

# Não se refira a um grupo de contas como "accountList" a menos que seja realmente uma list.
# Pode signifcar algo especifico, portanto "accountGroup" ou "bunchOfAccount" ou "accounts" seria melhor.

# Cuidado com nomes parecidos, isso pode atrapalhar o entendimento e a busca.

# Letras maiúsculas podem causar confusões, evita-las ao máximo.
